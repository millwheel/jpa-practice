    // 동적 쿼리 첫번째 방법. Boolean Builder 사용하기
    @Test
    public void 동적쿼리_BooleanBuilder() throws Exception {
        String usernameParam = "member1";
        Integer ageParam = 10;
        List<Member> result = searchMember1(usernameParam, ageParam);
        assertThat(result.size()).isEqualTo(1);
    }
    private List<Member> searchMember1(String usernameCond, Integer ageCond) {
        BooleanBuilder builder = new BooleanBuilder();
        if (usernameCond != null) {
            builder.and(member.username.eq(usernameCond));  // boolean builder에 and 조건 넣어주기
        }
        if (ageCond != null) {
            builder.and(member.age.eq(ageCond)); // boolean builder에 and 조건 넣어주기
        }
        // username과 age 둘 다 있으면 둘 다 조건에 들어가서 쿼리로 날라간다. 파라미터가 두개 바인딩
        return queryFactory
                .selectFrom(member)
                .where(builder)
                .fetch();
    }

    // 동적 쿼리 두 번째 방법. 훨씬 깔끔한 코드가 생성된다. BooleanExpression 사용하기
    @Test
    public void 동적쿼리_WhereParam() throws Exception {
        String usernameParam = "member1";
        Integer ageParam = 10;
        List<Member> result = searchMember2(usernameParam, ageParam);
        assertThat(result.size()).isEqualTo(1);
    }

    private List<Member> searchMember2(String usernameCond, Integer ageCond) {
        return queryFactory
                .selectFrom(member)
                .where(usernameEq(usernameCond), ageEq(ageCond))
                .fetch();
    }

    // 메서드를 이렇게 따로 빼놓으면 재활용을 하기에도 유용하다.
    private BooleanExpression usernameEq(String usernameCond) {
        return usernameCond != null ? member.username.eq(usernameCond) : null;

    }
    private BooleanExpression ageEq(Integer ageCond) {
        return ageCond != null ? member.age.eq(ageCond) : null;
    }
